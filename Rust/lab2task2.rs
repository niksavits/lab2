use std::io;

fn main() {
    // Задаем региональные настройки для вывода сообщений на русском языке
    println!("Введите количество элементов:");
    // Создаем переменную для хранения количества элементов
    let mut n = String::new();
    // Считываем ввод пользователя в переменную n
    io::stdin().read_line(&mut n).expect("Ошибка при чтении строки");
    // Преобразуем строку в число типа i32
    let n: i32 = n.trim().parse().expect("Введите целое число");

    println!("Введите числа:");
    // Создаем вектор для хранения чисел
    let mut nums: Vec<i32> = Vec::new();
    // Считываем n чисел и добавляем их в вектор
    for _ in 0..n {
        let mut num = String::new();
        io::stdin().read_line(&mut num).expect("Ошибка при чтении строки");
        let num: i32 = num.trim().parse().expect("Введите целое число");
        nums.push(num);
    }

    // Переменная для подсчета чисел с нечетным количеством цифр
    let mut count = 0;
    // Проходим по каждому числу в векторе
    for num in &nums {
        // Преобразуем число в строку и убираем пробелы
        let num_str = num.to_string().replace(" ", "");
        // Подсчитываем количество цифр в числе
        let digit_count = num_str.chars().filter(|c| c.is_digit(10)).count();
        // Если количество цифр нечетное, увеличиваем счетчик
        if digit_count % 2 != 0 {
            count += 1;
        }
    }

    // Выводим количество чисел с нечетным количеством цифр
    println!("Количество чисел с нечетным количеством цифр: {}", count);
}
